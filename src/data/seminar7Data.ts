function flow(title: string, items: string[]) {
  return { type: "flow" as const, title, items };
}

function cmp(title: string, leftTitle: string, rightTitle: string, rows: [string, string, string][]) {
  return { type: "comparison" as const, title, leftTitle, rightTitle, rows: rows.map(([label, left, right]) => ({ label, left, right })) };
}

